<pre><b><strong>छलफल र निर्णय </strong></b>
१. आदरणीय बुबाज्यू ८८ वर्ष पूरा भई ८९औं वर्षप्रवेशको शुभजन्मोत्सवको अवसरमा
   बुबाज्यू तथा मुमाज्यूको सुस्वास्थ्य, सुखशान्ति तथा दीर्घजीवनको लागि हार्दिक 
   शुभकामना गरियो। 
 
२. गुठीका कोषमा बचत खातामा रु ३,५६,००३।२३ र मुद्दती खातामा रु .५,१९,०००। 
   गरी जम्मा रु ८,५६,००३।२३ मौज्दात रहेको जानकारी कोषाध्यक्ष विकाश मान 
   श्रेष्ठले प्रस्तुत गर्नुभयो। 
  
३. सहकोषाध्यक्ष प्रमी श्रेष्ठ र सल्लाहकार सदस्य डा. हरि प्रसाद श्रेष्ठ मोटरसाइकल 
   दुर्घटनामा परि खुट्टामा चोटपटक लागेकोले वहांहरुको शिघ्र पूर्ण स्वास्थ्यलाभको 
   कामना गरियो। 
 
४. सदस्य यशस्वी श्रेष्ठ (लुसी)को डा. रोजेन्द्र सिंह (श्री सुरेन्द्र प्रसाद सिंह तथा 
   श्रीमति रोहिणी सिंहका सुपुत्र) का साथ शुभविवाह (स्वयम्बर मिति २०६७।०३।०३मा 
   स्वयम्बर) सम्पन्न भएकोले नवविवाहित दम्पतिको सुखमय दाम्पत्य जिवन तथा 
   उज्ज्वल भविष्यको शुभकामना दिइयो। 
 
५. सदस्य यशस्वी श्रेष्ठका श्रीमान डाक्टर श्री रोजेन्द्र सिंहलाई सल्लाहकार सदस्यता 
   प्रदान गर्ने निर्णय भयो। 
 
६. सदस्य अविरल मान श्रेष्ठले अस्ट्रेलियाको Wallangan University मा 
   Management faculty मा Economics विषयमा 
   distinction सहित Bachelor of Commerce उत्तीर्ण भै 
   graduation गर्नु भएकोले वहा तथा वहांका पिता विकास मान श्रेष्ठ तथा 
   माता रमिला श्रेष्ठ समेतलाई बधाई दिइयो। 
 
७. अंगीकृत सदस्य रमीला श्रेष्ठको नेपाल बैकको कर्मक्षेत्रमा अधिकृत पांचौस्तरमा 
   पदोन्नति भई मानबृद्धि भएकोले वहांलाई बधाइ दिइयो। 
 
८. छोरी यशस्वी श्रेष्ठको विवाह राम्रोसंग सुसम्पन्न भएको अवसरमा गुठीको कोषबृद्धिको 
   लागि सर्वप्रथम रु. ५,०००। (अक्षरुपी पाच हजारमात्र) प्रदान गर्नुभएको साथै, 
   आफनो छोरीको विवाह राम्रो संग गरिदिनु भएकोमा अध्यक्ष डा. पुष्पा श्रेष्ठ तथा 
   सल्ल्ाहकार सदस्य श्री कृष्ण भक्त श्रेष्ठज्यू लाई सधन्यवाद बधाई दिइयो। 
 
९. आफू ठूलो दुर्घटनामा परी बांच्न सफल भई पुनर्जिवन पाएको तथा आफनो बैवाहिक 
   जिवनको २४ वर्ष पूराभई २५सौं वर्षप्रवेशको अवसरमा सहकोषाध्यक्ष प्रमी श्रेष्ठ तथा 
   सल्लाहकार सदस्य डा. श्री हरि प्रसाद श्रेष्ठज्यूले गुठीको कोष बृद्धिको लागि रु 
   ५,०००। (अक्षरुपी पाच हजारमात्र) प्रदान गर्नुभएकोमा धन्यवाद दिइयो र वहांहरुको 
   प्रेममय दाम्पत्य जिवन अझ बढी सुखमय होस भनि शुभकामना दिइयो।    
 
१०. मानार्थ सदस्य श्रीमान ठाकुर प्रसाद थांजूको प्रथम नातिनी (जेठा छोरा प्रमोद र 
   बुहारि कृपाको जेष्ठ सन्तान मिति २०६३।..।.. मा) जन्म भएको अवसरमा 
   बधाई दिइयो। 
 
११. मानार्थ सदस्य श्री ठाकुर प्रसाद थांजू अस्वस्थताको कारणले यस मीटिंगमा उपस्थित 
   हुन नसक्नुभएको, साथै वहांकी श्रीमति (माइजू शोभा थांजू) एवम् अंगीकृत 
   सदस्य निलिना श्रेष्ठ दुवैको ढाड दुखिराखेकोले वहांहरु सबैको शिघ्र स्वास्थ्यलाभको 
   कामना गरियो। 
 
१२. सदस्य सुशी श्रेष्ठले वेलायतको Edenbergh स्थित Edenbergh 

   नभएको कारणले वित्तिय संस्थाले समेत ऋण नदिने अत्यन्तै दुखी विशेषत महिलालाई 
   कुनै संस्था मार्फत सहयोग गर्ने” विषयमा बालाजु स्थित “महिला सहयोगी सहकारी 
   संस्था लिमिटेड” संग कुराकानी गर्दा वहांहरुले प्रारम्भिक मौखिक सहमति जनाई 
   औपचारिक रुपमा प्रस्तावभै आउन अनुरोध गर्नुुभएकोले देहायको सेवाशर्त अन्तरगत 
